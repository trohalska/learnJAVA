package junit.arithmetic;

public class Arithmetic {
    public int add(int a, int b) {
        return a + b;
    }

    public int deduct(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public double divide(int a, int b) {
        return a / b;
    }
}
